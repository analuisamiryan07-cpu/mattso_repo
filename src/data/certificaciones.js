function req(edu, exp, cap, otro) {
  const r = [
    { number: '01', title: 'Educación', desc: edu },
    { number: '02', title: 'Experiencia', desc: exp },
  ];
  if (cap && cap !== 'Ninguna' && cap !== 'No aplica' && cap !== null) {
    r.push({ number: '03', title: 'Capacitación', desc: cap });
  }
  if (otro) r.push({ number: String(r.length + 1).padStart(2, '0'), title: 'Adicional', desc: otro });
  return r;
}

